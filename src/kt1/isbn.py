"""
Формат ISBN-10 - это код, состоящий из 9 цифр (от 0 до 9) и проверочного
символа (цифры или X). В случае, если проверочный символ это X, он означает число 10.

Обычно такие номера содержат разделительные дефисы: 3-598-21508-8,
хотя можно встретить и упрощенную запись 3598215088.

Для проверки номеров на корректность используется следующая формула:

(d₁ * 10 + d₂ * 9 + d₃ * 8 + d₄ * 7 + d₅ * 6 + d₆ * 5 + d₇ * 4 + d₈ * 3 + d₉ * 2 + d₁₀ * 1) mod 11 == 0

Если это равенство выполняется, номер валиден, иначе - нет.

Так как результат 0, номер корректен.
"""


def is_valid(isbn: str) -> bool:
    """Проверяет строку на соответствие ISBN-10
    по формату и проверочному правилу.

    Например, для кода 3-598-21508-8 мы получим следующее:
    (3 * 10 + 5 * 9 + 9 * 8 + 8 * 7 + 2 * 6 + 1 * 5 + 5 * 4 + 0 * 3 + 8 * 2 + 8 * 1) mod 11 -> 0

    >>> is_valid("3-598-21508-8")
    True

    :param isbn: str - код, с разделяющими дефисами или без них.
    :return: bool - логическое значение корректности.
    """
    
    isbn_clean = ""
    for char in isbn:
        if char != '-':
            isbn_clean += char
    
    if len(isbn_clean) != 10:
        return False
    
    for i in range(9):
        if not isbn_clean[i].isdigit():
            return False
    
    if not (isbn_clean[9].isdigit() or isbn_clean[9] == 'X'):
        return False
    
    total = 0
    for i in range(9):
        total += int(isbn_clean[i]) * (10 - i)
    
    if isbn_clean[9] == 'X':
        total += 10 * 1
    else:
        total += int(isbn_clean[9]) * 1
    
    return total % 11 == 0